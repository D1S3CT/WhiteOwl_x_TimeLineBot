from telegram import Update
from telegram.ext import ContextTypes
from bot.keyboards.main_menu_keyboard import get_main_menu_keyboard


async def show_welcome_message(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """
    –û—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ —Å –≥–ª–∞–≤–Ω—ã–º –º–µ–Ω—é.

    Args:
        update (Update): –û–±—ä–µ–∫—Ç –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –æ—Ç Telegram API
        context (ContextTypes.DEFAULT_TYPE): –ö–æ–Ω—Ç–µ–∫—Å—Ç –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è –∫–æ–º–∞–Ω–¥—ã

    Returns:
        None
    """
    keyboard = get_main_menu_keyboard()

    welcome_text = (
        "üåü –î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ TimeLineBot! üåü\n\n"
        "–Ø –ø–æ–º–æ–≥—É –≤–∞–º –Ω–∞–π—Ç–∏ –ª—É—á—à–µ–≥–æ –º–∞—Å—Ç–µ—Ä–∞ –∏–ª–∏ —Å—Ç–∞—Ç—å –º–∞—Å—Ç–µ—Ä–æ–º —Å–∞–º–æ–º—É!\n\n"
        "–í—ã–±–µ—Ä–∏—Ç–µ –¥–µ–π—Å—Ç–≤–∏–µ –Ω–∏–∂–µ üëá"
    )

    if update.message:
        await update.message.reply_text(
            welcome_text,
            reply_markup=keyboard
        )
    elif update.callback_query:
        await update.callback_query.message.edit_text(
            welcome_text,
            reply_markup=keyboard
        )
        await update.callback_query.answer()


